#!/usr/bin/env python3
from __future__ import annotations

import argparse
import csv
import json
from dataclasses import dataclass
from pathlib import Path
import sys

REPO_ROOT = Path(__file__).resolve().parents[1]
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))

from src.data.pressure_qdot_loader import load_pressure_qdot_manifest


@dataclass(slots=True)
class CleanStats:
    condition_id: str
    source_csv_path: str
    output_csv_path: str
    original_rows: int
    duplicate_groups_collapsed: int
    duplicate_rows_removed: int
    qdot_all_zero_rows_removed: int
    initial_pressure_baseline_rows_removed: int
    cleaned_rows: int
    time_start: float
    time_end: float
    duration_seconds: float
    dt_min_seconds: float
    dt_median_seconds: float
    dt_max_seconds: float


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Create a cleaned, non-destructive copy of pressure/Qdot CSV files.")
    parser.add_argument(
        "--manifest-json",
        type=str,
        default="./data/pressure_qdot_csv/manifest.json",
        help="Manifest JSON generated by export_pressure_qdot_csv.py.",
    )
    parser.add_argument(
        "--output-dir",
        type=str,
        default="./data/pressure_qdot_csv_clean",
        help="Directory to save cleaned CSV files and manifest.",
    )
    parser.add_argument(
        "--pressure-baseline",
        type=float,
        default=101325.0,
        help="Pressure baseline used to trim the initial all-baseline segment.",
    )
    return parser


def _aggregate_duplicate_times(rows: list[list[float]]) -> tuple[list[list[float]], int, int]:
    if not rows:
        return [], 0, 0

    aggregated: list[list[float]] = []
    duplicate_groups = 0
    duplicate_rows_removed = 0

    current_t = rows[0][0]
    current_group = [rows[0]]
    for row in rows[1:]:
        if row[0] == current_t:
            current_group.append(row)
        else:
            if len(current_group) > 1:
                duplicate_groups += 1
                duplicate_rows_removed += len(current_group) - 1
                n = len(current_group)
                means = [sum(values) / n for values in zip(*current_group)]
                aggregated.append(means)
            else:
                aggregated.append(current_group[0])
            current_t = row[0]
            current_group = [row]

    if len(current_group) > 1:
        duplicate_groups += 1
        duplicate_rows_removed += len(current_group) - 1
        n = len(current_group)
        means = [sum(values) / n for values in zip(*current_group)]
        aggregated.append(means)
    else:
        aggregated.append(current_group[0])

    return aggregated, duplicate_groups, duplicate_rows_removed


def _remove_qdot_all_zero(rows: list[list[float]], qdot_slice: slice) -> tuple[list[list[float]], int]:
    kept: list[list[float]] = []
    removed = 0
    for row in rows:
        q_vals = row[qdot_slice]
        if all(v == 0.0 for v in q_vals):
            removed += 1
            continue
        kept.append(row)
    return kept, removed


def _trim_initial_pressure_baseline(rows: list[list[float]], pressure_slice: slice, baseline: float) -> tuple[list[list[float]], int]:
    trim = 0
    for row in rows:
        p_vals = row[pressure_slice]
        if all(v == baseline for v in p_vals):
            trim += 1
        else:
            break
    return rows[trim:], trim


def _dt_stats(rows: list[list[float]]) -> tuple[float, float, float]:
    if len(rows) <= 1:
        return 0.0, 0.0, 0.0
    dts = [rows[i][0] - rows[i - 1][0] for i in range(1, len(rows))]
    dts_sorted = sorted(dts)
    return dts_sorted[0], dts_sorted[len(dts_sorted) // 2], dts_sorted[-1]


def _write_csv(path: Path, header: list[str], rows: list[list[float]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(header)
        writer.writerows(rows)


def _write_manifest(output_dir: Path, conditions: list[dict]) -> None:
    payload = {
        "source_type": "pressure_qdot_csv_clean",
        "output_dir": str(output_dir.resolve()),
        "conditions": conditions,
    }
    (output_dir / "manifest.json").write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")


def _write_cleaning_summary(output_dir: Path, stats: list[CleanStats]) -> None:
    fieldnames = [
        "工况编号",
        "原始CSV路径",
        "清洗后CSV路径",
        "原始行数",
        "重复时间组数",
        "重复时间删除行数",
        "Qdot全零删除行数",
        "初始压力基线删除行数",
        "清洗后行数",
        "起始时间_秒",
        "结束时间_秒",
        "持续时长_秒",
        "最小时间步长_秒",
        "中位时间步长_秒",
        "最大时间步长_秒",
    ]
    with (output_dir / "cleaning_summary.csv").open("w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        for s in stats:
            writer.writerow(
                {
                    "工况编号": s.condition_id,
                    "原始CSV路径": s.source_csv_path,
                    "清洗后CSV路径": s.output_csv_path,
                    "原始行数": s.original_rows,
                    "重复时间组数": s.duplicate_groups_collapsed,
                    "重复时间删除行数": s.duplicate_rows_removed,
                    "Qdot全零删除行数": s.qdot_all_zero_rows_removed,
                    "初始压力基线删除行数": s.initial_pressure_baseline_rows_removed,
                    "清洗后行数": s.cleaned_rows,
                    "起始时间_秒": s.time_start,
                    "结束时间_秒": s.time_end,
                    "持续时长_秒": s.duration_seconds,
                    "最小时间步长_秒": s.dt_min_seconds,
                    "中位时间步长_秒": s.dt_median_seconds,
                    "最大时间步长_秒": s.dt_max_seconds,
                }
            )


def _write_cleaning_report(output_dir: Path, stats: list[CleanStats], baseline: float) -> None:
    lines = []
    lines.append("# 清洗报告")
    lines.append("")
    lines.append("## 清洗规则")
    lines.append("1. 对相同时间戳的多行做均值聚合。")
    lines.append("2. 删除 `Qdot` 16 路全零的行。")
    lines.append(f"3. 删除开头连续的 16 路压力全 `{baseline} Pa` 基线段。")
    lines.append("")
    lines.append("## 工况级结果")
    lines.append("")
    lines.append("| 工况编号 | 原始行数 | 重复时间删除 | Qdot全零删除 | 压力基线删除 | 清洗后行数 | 起始时间(s) | 结束时间(s) | dt中位数(s) |")
    lines.append("|---|---:|---:|---:|---:|---:|---:|---:|---:|")
    for s in stats:
        lines.append(
            f"| `{s.condition_id}` | `{s.original_rows}` | `{s.duplicate_rows_removed}` | `{s.qdot_all_zero_rows_removed}` | "
            f"`{s.initial_pressure_baseline_rows_removed}` | `{s.cleaned_rows}` | `{s.time_start:.9g}` | `{s.time_end:.9g}` | `{s.dt_median_seconds:.9g}` |"
        )
    (output_dir / "cleaning_report.md").write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> None:
    args = build_parser().parse_args()
    manifest = load_pressure_qdot_manifest(args.manifest_json)
    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    cleaned_conditions: list[dict] = []
    stats: list[CleanStats] = []

    for condition in manifest["conditions"]:
        source_csv = Path(condition["csv_path"])
        with source_csv.open("r", encoding="utf-8", newline="") as f:
            reader = csv.reader(f)
            header = next(reader)
            rows = [[float(v) for v in row] for row in reader]

        original_rows = len(rows)
        if not rows:
            raise ValueError(f"No rows in {source_csv}")

        p_cols = [i for i, name in enumerate(header) if name.startswith("p")]
        q_cols = [i for i, name in enumerate(header) if name.startswith("qdot")]
        pressure_slice = slice(min(p_cols), max(p_cols) + 1)
        qdot_slice = slice(min(q_cols), max(q_cols) + 1)

        rows, duplicate_groups, duplicate_rows_removed = _aggregate_duplicate_times(rows)
        rows, qdot_zero_removed = _remove_qdot_all_zero(rows, qdot_slice)
        rows, pressure_baseline_removed = _trim_initial_pressure_baseline(rows, pressure_slice, args.pressure_baseline)

        if not rows:
            raise ValueError(f"Cleaning removed all rows for {condition['condition_id']}")

        output_csv_name = Path(condition["csv_filename"]).name
        output_csv_path = output_dir / output_csv_name
        _write_csv(output_csv_path, header, rows)

        dt_min, dt_median, dt_max = _dt_stats(rows)
        time_start = rows[0][0]
        time_end = rows[-1][0]
        cleaned_conditions.append(
            {
                "condition_id": condition["condition_id"],
                "bypass_ratio": condition["bypass_ratio"],
                "csv_filename": output_csv_name,
                "csv_path": str(output_csv_path.resolve()),
                "source_csv_path": str(source_csv.resolve()),
                "n_probes": condition["n_probes"],
                "n_samples": len(rows),
                "dt_seconds": dt_median,
                "sample_rate_hz": (1.0 / dt_median) if dt_median > 0 else 0.0,
                "time_start": time_start,
                "time_end": time_end,
                "duration_seconds": time_end - time_start,
                "probe_coordinates": condition["probe_coordinates"],
                "cleaning": {
                    "duplicate_groups_collapsed": duplicate_groups,
                    "duplicate_rows_removed": duplicate_rows_removed,
                    "qdot_all_zero_rows_removed": qdot_zero_removed,
                    "initial_pressure_baseline_rows_removed": pressure_baseline_removed,
                    "pressure_baseline_value": args.pressure_baseline,
                },
            }
        )
        stats.append(
            CleanStats(
                condition_id=condition["condition_id"],
                source_csv_path=str(source_csv.resolve()),
                output_csv_path=str(output_csv_path.resolve()),
                original_rows=original_rows,
                duplicate_groups_collapsed=duplicate_groups,
                duplicate_rows_removed=duplicate_rows_removed,
                qdot_all_zero_rows_removed=qdot_zero_removed,
                initial_pressure_baseline_rows_removed=pressure_baseline_removed,
                cleaned_rows=len(rows),
                time_start=time_start,
                time_end=time_end,
                duration_seconds=time_end - time_start,
                dt_min_seconds=dt_min,
                dt_median_seconds=dt_median,
                dt_max_seconds=dt_max,
            )
        )

    _write_manifest(output_dir, cleaned_conditions)
    _write_cleaning_summary(output_dir, stats)
    _write_cleaning_report(output_dir, stats, args.pressure_baseline)

    print("Cleaning finished.")
    print(f"Output dir: {output_dir}")
    print(f"Manifest: {output_dir / 'manifest.json'}")
    print(f"Cleaning summary: {output_dir / 'cleaning_summary.csv'}")
    print(f"Cleaning report: {output_dir / 'cleaning_report.md'}")


if __name__ == "__main__":
    main()
